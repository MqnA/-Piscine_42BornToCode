int	ft_atoi(char *str)
{
	int	nb;
	int	i;

	i = 0;
	nb = 0;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + ('0' - str[i]);
		i++;
	}
	if (str[0] == '-')
		nb = -nb;
	return (nb);
}
