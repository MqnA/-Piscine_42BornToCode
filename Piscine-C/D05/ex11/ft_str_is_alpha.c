int		ft_str_is_alpha(char *str)
{
	int a;

	a = 0;
	while ((str[a] >= 'A' && str[a] <= 'Z') || (str[a] >= 'a' && str[a] <= 'z'))
		a++;
	if (str[a] == '\0')
		return (1);
	return (0);
}
